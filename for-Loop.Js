/* for loop
loops are used to execute a piece of code again & again
*/


// Print 1 to 5 
for(let i = 1; i<=5; i++){
  console.log("i =",i);
}

// Calculate sum of 1 to 5
let sum = 0;
let n = 5
for(let i = 1; i<=n; i++){
  sum += i; // sum = sum + i
}
console.log("sum =",sum); // sum = 15

